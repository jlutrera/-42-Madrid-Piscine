int	ft_iterative_power(int nb, int power)
{
	int	r;
	int	i;

	if (power < 0)
		return (0);
	else
	{	
		r = 1;
		i = 0;
		while (i < power)
		{
			r *= nb;
			i++;
		}
	}
	return (r);
}
